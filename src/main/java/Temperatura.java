
public class Temperatura {

    //Atributo privado
    private double graus;

    //Construtor
    public Temperatura(double grausTemp) {
        this.graus = grausTemp;
    }

    //Getter
    public double getGraus() {
        return graus;
    }

    //Seter
    public void seterGraus(double novoGraus) {
        this.graus = novoGraus;
    }
}
